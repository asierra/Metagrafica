double text_width(TextState ts, string s) {
  if (ts.font_face==FN_COURIER) 
    return s.length() * 600 * ts.font_size / 1000;

  double width = 0;
  FontMetricsMap fmmap;

  switch (ts.font_face) {
  default:
  case FN_SERIF:
    fmmap = serif_metrics_map;
    break;
  case FN_SERIF_BOLD:
    fmmap = serifbold_metrics_map;
    break;
  case FN_SERIF_ITALIC:
    fmmap = serifitalic_metrics_map;
    break;
  case FN_SYMBOL:
    fmmap = symbol_metrics_map;
    break;
  case FN_TEX_CMMI:
    fmmap = cmmi_metrics_map;
    break;
  case FN_SANSERIF:
    fmmap = sanserif_metrics_map;
    break;
  case FN_SANSERIF_ITALIC:
    fmmap = sanserifitalic_metrics_map;
    break;
  case FN_SANSERIF_ITALIC_BOLD:
  case FN_SANSERIF_BOLD:
    fmmap = sanserifbold_metrics_map;
    break;
  }
  for (auto &ch : s) {
    width += fmmap[ch] * ts.font_size / 1000;
  }
  return width;
}

void Text::draw(Display &g) {
  // FN_NOFACE = "hereda la cara ambiente" (§7.3). Se resuelve contra la ambiente que
  // TextLine::draw guardó ANTES de la línea; solo si nadie la fijó nunca se cae al
  // comportamiento viejo (no llamar a setFontFace y dejar que los guards de
  // EPSDisplay/PDFDisplay pongan Times-Roman).
  //
  // ⚠️ El orden importa y es la mitad del bug que esto arregla: setRelFontSize
  // INVALIDA dspstate.fontFace (lo pone en FN_NOFACE) justo para forzar que la
  // siguiente llamada a setFontFace re-emita la fuente al tamaño nuevo. Si el trozo
  // se salta setFontFace, esa re-emisión NUNCA ocurre y el dispositivo se queda con
  // la fuente del trozo anterior: la cara Y el tamaño. Por eso la prosa que sigue a
  // un sub/superíndice salía chica, y la que sigue a un `$…$` salía en LM Math.
  FontFace face = textstate.font_face;
  if (face == FN_NOFACE) face = g.getInheritedFace();
  g.setRelFontSize(textstate.font_size);
  if (face != FN_NOFACE)
    g.setFontFace(face);
  // valign para texto SIMPLE (parse_text devuelve un Text pelón, sin TextLine):
  // desplaza la pluma una vez antes de emitir. En texto compuesto este Text se
  // dibuja desde TextLine::draw, que ya puso valign a 0 (aplica el desplazamiento
  // a la línea entera), así que aquí no se duplica. No se restaura: es estado
  // persistente y cada text() se reposiciona con un moveto absoluto propio.
  if (g.getTextValign() > 0)
    g.rmoveto(0, g.valignShift());
  g.text(text);
}

void TextLine::draw(Display &g) {
  double vdesp;

  int text_align = g.getTextAlign();
  int text_valign = g.getTextValign();
  if (text_align > 0 || text_valign > 0) {
    // dx solo cuando hay alineación horizontal: con align=0 (left) el factor
    // width*fs/(3-align) NO es cero (=width*fs/3), colaría un corrimiento espurio
    // si valign viene solo. dy (valign) sale del helper centralizado del Display.
    double dx = (text_align > 0) ? width() * g.getFontSize() / (3 - text_align) : 0.0;
    g.rmoveto(-dx, g.valignShift());
    g.setTextAlign(0);
    g.setTextValign(0);   // se puso a 0 para que los Text hijos no re-apliquen valign
  }
  // Cara AMBIENTE de la línea, capturada antes de dibujar ningún trozo: para cuando
  // un trozo se dibuja, la cara vigente ya puede ser la que puso un trozo ANTERIOR
  // (un run de `$…$` deja el dispositivo en LM Math), que no es la ambiente. Un
  // trozo con FN_NOFACE ("hereda la ambiente") la resuelve contra esto (Text::draw).
  //
  // Sin ella, FN_NOFACE estaba implementado como NO TOCAR el dispositivo, que solo
  // equivale a "la ambiente" mientras nadie la haya tocado. Dentro de una línea sí
  // la tocan, y se veía en cualquier rótulo de eje o entrada de leyenda que mezcle
  // fórmula y prosa: la prosa tras `$…$` salía en la fuente del math, y la prosa
  // tras un sub/superíndice salía al tamaño reducido del índice (ver Text::draw:
  // saltarse setFontFace también se salta el refresco de TAMAÑO). Un `text()` normal
  // nunca lo sufrió porque hornea FN_DEFAULT en todos sus trozos.
  // Si nadie fijó cara en el documento (lo normal en una figura que nunca escribe
  // `font`), la ambiente ES la cara por default — el mismo criterio que ya aplican
  // los guards de EPSDisplay::text/PDFDisplay::text al primer texto del documento, y
  // el que usa change_font_face para los modificadores de bit sobre FN_NOFACE.
  // Resolverla aquí es lo que hace que el arreglo sirva en una figura sin `font`,
  // que es justo donde vive el bug (los rótulos de axis/legend nacen con FN_NOFACE).
  { FontFace amb = g.getFontFace();
    g.setInheritedFace(amb != FN_NOFACE ? amb : FN_TIMES_ROMAN); }
  for (const auto &text : textline) {
    TextState ts = text->getState();
    //printf("text <%s> state %s\n", text->getText().c_str(), ts.str().c_str());
    if (ts.script != 0) {
      vdesp = (ts.script < 0) ? -g.getFontSize() * 0.14 : g.getFontSize() * 0.56;
      g.rmoveto(0, vdesp);
    }
    text->draw(g);
    if (ts.script != 0) {
      g.rmoveto(0, -vdesp);
    }
  }
  g.setInheritedFace(FN_NOFACE);    // fuera de la línea no hay ambiente que heredar
  if (text_align > 0)
    g.setTextAlign(text_align);
  if (text_valign > 0)
    g.setTextValign(text_valign);   // estado persistente: vale para los text() siguientes
}

void TextBlock::draw(Display &g) {
  if (lines.empty()) return;

  // El ancla del bloque: la pluma tal como la dejó el GS_PLUMEPOSITION previo. Se
  // relee para CADA renglón porque TextLine::draw mueve la pluma (corrimiento de
  // alineación + avance del texto), así que no se puede encadenar con movimientos
  // relativos en x.
  point anchor;
  g.getPlumePosition(anchor);

  // El interlineado sale del tamaño de fuente VIGENTE, que es la razón de que esto
  // viva en el motor: en parse-time no se conoce. rmoveto no transforma (va en
  // unidades de dispositivo), igual que el desplazamiento de sub/superíndices de
  // TextLine::draw, así que `lead` se usa tal cual.
  const double lead = leading * g.getFontSize();
  const std::size_t n = lines.size();

  // valign se aplica AL BLOQUE, no a cada renglón: `middle` centra el conjunto. La
  // alineación fina dentro de cada renglón la sigue haciendo Text/TextLine con el
  // valign vigente, que no se toca. Con un solo renglón y0 = 0 → comportamiento
  // idéntico al de antes (cero churn en todo el corpus).
  double y0 = 0;
  switch (g.getTextValign()) {
    case 2: y0 = lead * (double)(n - 1) / 2.0; break;   // middle
    case 3: y0 = lead * (double)(n - 1);       break;   // bottom
    default: break;                                     // baseline / top: 1ª línea en el ancla
  }

  for (std::size_t i = 0; i < n; i++) {
    if (!lines[i]) continue;                            // renglón vacío: solo consume interlineado
    g.moveto_nopath(anchor.x, anchor.y);
    g.rmoveto(0, y0 - lead * (double)i);
    lines[i]->draw(g);
  }
}

double TextLine::width() {
  double w = 0;
  for (const auto &text : textline) {
    TextState ts = text->getState();
    w += text_width(ts, text->getText());
  }
  return w;
}
